class Caluculator:

    def __init__(self, a = 0, b = 20) -> None:
        self.b = b
        self.a = a

    def addition(self) -> int:
        return self.a+self.b
    
    def subtraction(self) -> float:
        return self.a-self.b

    def multiplication(self) -> float:
        return self.a * self.b

    

